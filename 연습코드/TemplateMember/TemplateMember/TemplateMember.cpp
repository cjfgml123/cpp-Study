// TemplateMember.cpp : 이 파일에는 'main' 함수가 포함됩니다. 거기서 프로그램 실행이 시작되고 종료됩니다.
//

#include <iostream>
using namespace std;

template<typename T>
class CTest
{
public:
	// 생성자 선언
	CTest();
	T TestFunc();

protected:
	//정적 멤버 데이터 선언
	static T m_Data;
};

// 생성자 정의
template<typename T>
CTest<T>::CTest()
{

}

// 멤버 함수 정의
template<typename T>
T CTest<T>::TestFunc()
{
	return m_Data;
}

// 정적 멤버 변수 정의
template<typename T>
T CTest<T>::m_Data = 15;

int main()
{
	CTest<double> a;
	cout << a.TestFunc() << endl;

	return 0;
}

